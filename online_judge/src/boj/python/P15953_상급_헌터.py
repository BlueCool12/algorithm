first = [500] * 1 + [300] * 2 + [200] * 3 + [50] * 4 + [30] * 5
second = [512] * 1 + [256] * 2 + [128] * 4 + [64] * 8 + [32] * 16 + [0]

for i in range(int(input())):
    before, after = map(int, input().rstrip().split())
    answer = 0
    if (before < len(first)):
        answer += first[before]
    
    if (after < len(second)):
        answer += second[after]

    print(answer)